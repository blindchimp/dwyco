/* Encode a single block's worth of coefficients */

int
encode_one_block (JCOEFPTR block, int& last_dc_val,
		  c_derived_tbl *dctbl, c_derived_tbl *actbl,
			Packbits& pb, BITBUFT*& outbuf, int cpu_saver, unsigned int tweaker)
{
  int temp, temp2;
  int nbits;
  int k, r, i;
  
  /* Encode the DC coefficient difference per section F.1.2.1 */
  
  temp = temp2 = block[0] - last_dc_val;
	last_dc_val = block[0];

  if (temp < 0) {
    temp = -temp;		/* temp is abs value of input */
    /* For a negative input, want temp2 = bitwise complement of abs(input) */
    /* This code assumes we are on a two's complement machine */
    temp2--;
  }
  
  /* Find the number of bits needed for the magnitude of the coefficient */
  nbits = 0;
  while (temp) {
    nbits++;
    temp >>= 1;
  }
  
  /* Emit the Huffman-coded symbol for the number of bits */
	pb.addbits_raw(dctbl->ehufco[nbits], dctbl->ehufsi[nbits], outbuf);

  /* Emit that number of bits of the value, if positive, */
  /* or the complement of its magnitude, if negative. */
  if (nbits)			/* emit_bits rejects calls with size 0 */
    pb.addbits_raw((unsigned int) temp2 ^ tweaker, nbits, outbuf);

  /* Encode the AC coefficients per section F.1.2.2 */
  
  r = 0;			/* r = run length of zeros */
  
  for (k = 1; k < cpu_saver; k++) {
    if ((temp = block[jpeg_natural_order[k]]) == 0) {
      r++;
    } else {
      /* if run length > 15, must emit special run-length-16 codes (0xF0) */
      while (r > 15) {
			pb.addbits_raw(actbl->ehufco[0xF0], actbl->ehufsi[0xF0], outbuf);
			r -= 16;
      }

      temp2 = temp;
      if (temp < 0) {
	temp = -temp;		/* temp is abs value of input */
	/* This code assumes we are on a two's complement machine */
	temp2--;
      }
      
      /* Find the number of bits needed for the magnitude of the coefficient */
      nbits = 1;		/* there must be at least one 1 bit */
      while ((temp >>= 1))
	nbits++;
      
      /* Emit Huffman symbol for run length / number of bits */
      i = (r << 4) + nbits;
      pb.addbits_raw(actbl->ehufco[i], actbl->ehufsi[i], outbuf);

      /* Emit that number of bits of the value, if positive, */
      /* or the complement of its magnitude, if negative. */
      pb.addbits_raw((unsigned int) temp2 ^ tweaker, nbits, outbuf);
      
      r = 0;
    }
  }

	if(k < DCTSIZE2)
		r = DCTSIZE2 - k;
  /* If the last coef(s) were zero, emit an end-of-block code */
  if (r > 0)
    pb.addbits_raw(actbl->ehufco[0], actbl->ehufsi[0], outbuf);

  return TRUE;
}


